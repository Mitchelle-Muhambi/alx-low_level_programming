#include <stdio.h>

/**
 * add - add two intergers
 * @a: first unit
 * @b: second unit
 * Return: addition
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * subtract - subtract two intergers
 * @a: first unit
 * @b: second unit
 * Return: subtracted final result
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mod -  mod of two intergers
 * @a: first unit
 * @b: second unit
 * Return: final answer
 */
int mod(int a, int b)
{
	return (a % b);
}
/**
 * mul - multiplys two intergers
 * @a: first unit
 * @b: second unit
 * Return: multiplication
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * div - divides two intergers
 * @a: first unit
 * @b: second unit
 * Return: division
 */
int div(int a, int b)
{
	return (a / b);
}
